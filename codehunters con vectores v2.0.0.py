    pl.speed(velocidad)
    pl.pensize(4)
    pl.fillcolor("green")
    pl.pencolor("green")
    pl.begin_fill()
    pl.left(90)
    pl.forward(150)
    pl.left(-120)
    pl.forward(100)
    pl.right(-90)
    pl.forward(80)
    pl.left(90)
