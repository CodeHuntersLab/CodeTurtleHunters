def dibuja_bordes(velocidad):
    pl = turtle.Turtle()
    pl.speed(velocidad)
    pl.pensize(50)
    pl.penup()
    pl.left(90)
    pl.backward(190)
    pl.left(90)
    pl.backward(30)
    pl.pendown()
    pl.left(180)
    pl.forward(120)
    pl.right(90)
    pl.backward(270)
    pl.penup()
    pl.right(180)
    pl.forward(180)
    pl.right(90)
    pl.backward(190)
    pl.pendown()
    pl.right(180)
    pl.forward(260)
    pl.left(90)
    pl.forward(140)
    pl.penup()
    pl.left(180)
    pl.backward(140)
    pl.pendown()
    pl.right(180)
    pl.forward(170)
    pl.left(90)
    pl.forward(120)

